def get_location_translation(location: str, target_lang: str) -> str | None:
    """
    震源地名の翻訳を取得

    Args:
        location: 日本語の震源地名
        target_lang: 翻訳先言語コード

    Returns:
        翻訳された地名（見つからない場合はNone）
    """
    if location in LOCATION_TRANSLATIONS:
        return LOCATION_TRANSLATIONS[location].get(target_lang)
    return None


def get_all_locations() -> list[str]:
    """
    登録されている全ての震源地名を取得

    Returns:
        震源地名リスト
    """
    return list(LOCATION_TRANSLATIONS.keys())


